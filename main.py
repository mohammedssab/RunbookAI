@app.post("/api/execute")
async def execute_script(req: ExecRequest):
    try:
        # Run command locally with a 15 second timeout to prevent hangs
        result = subprocess.run(
            req.script, 
            shell=True, 
            text=True, 
            capture_output=True, 
            timeout=15
        )
        return {
            "exit_code": result.returncode,
            "stdout": result.stdout,
            "stderr": result.stderr
        }
    except subprocess.TimeoutExpired as e:
        return {
            "exit_code": -1,
            "stdout": e.stdout or "",
            "stderr": f"Execution timed out after 15 seconds.\nStderr output so far: {e.stderr or ''}"
        }
    except Exception as e:
        return {
            "exit_code": -1,
            "stdout": "",
            "stderr": str(e)
        }

@app.get("/api/health")
async def health_check():
    ollama_ok = False
    try:
        # Check if Ollama tags endpoint is reachable
        tags_url = OLLAMA_URL.rsplit("/", 1)[0] + "/tags"
        res = requests.get(tags_url, timeout=2)
        if res.status_code == 200:
            ollama_ok = True
    except Exception:
        pass
    return {
        "backend": "ok",
        "ollama": "ok" if ollama_ok else "offline"
    }


class LogRequest(BaseModel):
    log_text: str

class ExecRequest(BaseModel):
    script: str

OLLAMA_URL = os.environ.get("OLLAMA_URL", "http://localhost:11434/api/generate")

def parse_ollama_response(raw_text: str):
    """
    Parses Ollama output to extract the root cause and the fix script block.
    """
    root_cause = ""
    fix_script = ""
    
    # Extract Root Cause (look for ROOT CAUSE: and stop before FIX SCRIPT or backticks)
    rc_match = re.search(r'(?i)root\s*cause\s*:\s*(.*?)(?=\n\s*(?:fix\s*script|```|$))', raw_text, re.DOTALL)
    if rc_match:
        root_cause = rc_match.group(1).strip()
    else:
        # Fallback to searching line-by-line
        for line in raw_text.splitlines():
            if line.upper().startswith("ROOT CAUSE:"):
                root_cause = line[11:].strip()
                break
    
    # Extract Fix Script (look for code blocks)
    code_blocks = re.findall(r'```(?:bash|sh|shell)?\n(.*?)\n```', raw_text, re.DOTALL | re.IGNORECASE)
    if code_blocks:
        fix_script = code_blocks[0].strip()
    else:
        # Fallback to general code block backticks
        code_blocks_generic = re.findall(r'```(.*?)\n```', raw_text, re.DOTALL)
        if code_blocks_generic:
            fix_script = code_blocks_generic[0].strip()
        else:
            # Fallback to everything after FIX SCRIPT: header
            parts = re.split(r'(?i)fix\s*script\s*:', raw_text)
            if len(parts) > 1:
                fix_script = parts[1].strip()
                
    # Set default values if parsing was completely empty
    if not root_cause:
        root_cause = "Could not parse root cause. See raw analysis response below."
    if not fix_script:
        fix_script = "# No executable fix script was generated by the AI."
        
    return {
        "root_cause": root_cause,
        "fix_script": fix_script
    }

@app.post("/api/analyze")
async def analyze_log(req: LogRequest):
    prompt = (
        f"You are a Linux SysAdmin. Analyze this error:\n{req.log_text}\n\n"
        f"Respond ONLY in this format:\n"
        f"ROOT CAUSE: <one sentence reason>\n"
        f"FIX SCRIPT:\n"
        f"```bash\n"
        f"<commands>\n"
        f"```"
    )
    
    try:
        response = requests.post(OLLAMA_URL, json={
            "model": "gemma4:e2b",
            "prompt": prompt,
            "stream": False
        }, timeout=300)
        
        if response.status_code != 200:
            raise HTTPException(status_code=500, detail=f"Ollama returned status {response.status_code}: {response.text}")
            
        raw_analysis = response.json().get("response", "")
        parsed = parse_ollama_response(raw_analysis)
        
        return {
            "raw_analysis": raw_analysis,
            "root_cause": parsed["root_cause"],
            "fix_script": parsed["fix_script"]
        }
    except requests.exceptions.RequestException as e:
        return {
            "raw_analysis": f"Error connecting to Ollama: {str(e)}",
            "root_cause": "Ollama service is unreachable. Make sure Ollama is running locally and model gemma4:e2b is loaded.",
            "fix_script": "# Ollama offline. Cannot diagnose automatically.\necho 'Error: Ollama connection failed.'"
        }

